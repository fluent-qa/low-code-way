CREATE TABLE `information_schema`.`TABLES`  (
  `TABLE_CATALOG` varchar(64) CHARACTER SET utf8 COLLATE utf8_bin NOT NULL,
  `TABLE_SCHEMA` varchar(64) CHARACTER SET utf8 COLLATE utf8_bin NOT NULL,
  `TABLE_NAME` varchar(64) CHARACTER SET utf8 COLLATE utf8_bin NOT NULL,
  `TABLE_TYPE` enum('BASE TABLE','VIEW','SYSTEM VIEW') CHARACTER SET utf8 COLLATE utf8_bin NOT NULL,
  `ENGINE` varchar(64) CHARACTER SET utf8 COLLATE utf8_general_ci NULL DEFAULT NULL,
  `VERSION` int(2) NULL DEFAULT NULL,
  `ROW_FORMAT` enum('Fixed','Dynamic','Compressed','Redundant','Compact','Paged') CHARACTER SET utf8 COLLATE utf8_bin NULL DEFAULT NULL,
  `TABLE_ROWS` bigint(21) UNSIGNED NULL DEFAULT NULL,
  `AVG_ROW_LENGTH` bigint(21) UNSIGNED NULL DEFAULT NULL,
  `DATA_LENGTH` bigint(21) UNSIGNED NULL DEFAULT NULL,
  `MAX_DATA_LENGTH` bigint(21) UNSIGNED NULL DEFAULT NULL,
  `INDEX_LENGTH` bigint(21) UNSIGNED NULL DEFAULT NULL,
  `DATA_FREE` bigint(21) UNSIGNED NULL DEFAULT NULL,
  `AUTO_INCREMENT` bigint(21) UNSIGNED NULL DEFAULT NULL,
  `CREATE_TIME` timestamp(0) NOT NULL,
  `UPDATE_TIME` datetime(0) NULL DEFAULT NULL,
  `CHECK_TIME` datetime(0) NULL DEFAULT NULL,
  `TABLE_COLLATION` varchar(64) CHARACTER SET utf8 COLLATE utf8_general_ci NULL DEFAULT NULL,
  `CHECKSUM` bigint(21) NULL DEFAULT NULL,
  `CREATE_OPTIONS` varchar(256) CHARACTER SET utf8 COLLATE utf8_general_ci NULL DEFAULT NULL,
  `TABLE_COMMENT` text CHARACTER SET utf8 COLLATE utf8_general_ci NULL
);
